package com.sungho.vo;

public class MovieVO {
	private String movieTitle;
    private String movieDirector;
    private String mainActor;
    private String stealCutUrl; 
    private int asterion;
    private String summary;
	public String getMovieTitle() {
		return movieTitle;
	}
	public void setMovieTitle(String movieTitle) {
		this.movieTitle = movieTitle;
	}
	public String getMovieDirector() {
		return movieDirector;
	}
	public void setMovieDirector(String movieDirector) {
		this.movieDirector = movieDirector;
	}
	public String getMainActor() {
		return mainActor;
	}
	public void setMainActor(String mainActor) {
		this.mainActor = mainActor;
	}
	public String getStealCutUrl() {
		return stealCutUrl;
	}
	public void setStealCutUrl(String stealCutUrl) {
		this.stealCutUrl = stealCutUrl;
	}
	public int getAsterion() {
		return asterion;
	}
	public void setAsterion(int asterion) {
		this.asterion = asterion;
	}
	public String getSummary() {
		return summary;
	}
	public void setSummary(String summary) {
		this.summary = summary;
	}
    
    
    
}
